SELECT R, S_NAME, S_KOR, S_ENG, S_MATH, S_SUM, S_AVG, S_GRADE
FROM
(SELECT ROWNUM AS R, S_NAME, S_KOR, S_ENG, S_MATH, S_SUM, S_AVG, S_GRADE
FROM
	(SELECT S_NAME, S_KOR, S_ENG, S_MATH, S_SUM, S_AVG, S_GRADE
	FROM SCORE
	ORDER BY S_AVG DESC)A
)B
WHERE R = 3;